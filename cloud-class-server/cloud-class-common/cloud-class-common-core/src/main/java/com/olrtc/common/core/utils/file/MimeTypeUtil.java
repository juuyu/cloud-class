    private static final    Map<String, String> MIME_TYPE       = new HashMap<>(1024);
    private static final    String              defaultMimeType = "application/octet-stream";
    private static volatile boolean             loaded          = false;

    /**
     * 根据文件后缀推断文件的mime类型, 找不到则使用下载类型
     *
     * @param fileName fileName
     * @return java.lang.String
     */
    public static String probeContentType(String fileName) {
        return probeContentType(fileName, defaultMimeType);
    }

    /**
     * 根据文件后缀推断文件的mime类型, 找不到则使用defaultMimeType
     *
     * @param fileName        fileName
     * @param defaultMimeType defaultMimeType
     * @return java.lang.String
     */
    public static String probeContentType(String fileName, String defaultMimeType) {
        String res = defaultMimeType;
        String ext = FileUtil.getExtension(fileName);
        if (!ext.isEmpty()) {
            loadMimeTypes();
            res = MIME_TYPE.getOrDefault(ext, defaultMimeType);
        }
        return res;
    }

    private static void loadMimeTypes() {
        if (!loaded) {
            synchronized (MimeTypeUtil.MIME_TYPE) {
                if (!loaded) {
                    if (CollectionUtils.isEmpty(MIME_TYPE)) {
                        load();
                    }
                    loaded = true;
                }
            }
        }
    }

